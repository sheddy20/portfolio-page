function add(num1, num2){
    let a = num1 + num2;
    return a;
}

function subtract(num1, num2){
    let b = num1 - num2;
    return b;
}

function divide(num1, num2){
    let c = num1 / num2;
    return c;
}

function multiply(num1, num2){
    let d = num1 * num2;
    return d;
}

let answer1 = add(10, 5);
let answer2 = subtract(10, 5);
let answer3 = divide(10, 2);
let answer4 = multiply(10, 4);

console.log(answer1);
console.log(answer2);
console.log(answer3);
console.log(answer4);